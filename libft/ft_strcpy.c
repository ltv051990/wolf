#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t in;

	in = 0;
	while (src[in] != '\0')
	{
		dst[in] = src[in];
		in++;
	}
	dst[in] = '\0';
	return (dst);
}
