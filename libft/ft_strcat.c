#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t index;

	index = ft_strlen(s1);
	while (*s2 != '\0')
	{
		s1[index] = *s2;
		index++;
		s2++;
	}
	s1[index] = '\0';
	return (s1);
}
